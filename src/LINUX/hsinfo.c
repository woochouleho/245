extern mib_table_entry_T mib_table[];

static int is_key_item(const char *name) {
	int i;
	for (i=0; i < ARRAY_SIZE(key_items); i++) {
		if (!strcmp(name,key_items[i]))
			return 1;
	}
	return 0;
}

static void usage(void)
{
	printf("Usage: hswlan cmd\n");
	printf("cmd:\n");
	printf("  is_empty [verbose]    return 1 if wlan hs is empty\n");
	printf("  sync [force]          sync " OLD_XML_FILE " to hs\n");
}

static int wlan_is_empty(mib_table_entry_T **which) {
	mib_table_entry_T *ptr;
	uint8_t buf[512];
	unsigned char tmp;
	int i;

	for (ptr = mib_table; ptr->id; ptr++) {
		if (ptr->mib_type != HW_SETTING)
			continue;
		if (!is_key_item(ptr->name))
			continue;
		//printf("%s(%d): %s\n",__func__,__LINE__,ptr->name);
		if (ptr->size > sizeof(buf)) {
			printf("ERROR: entry size(%d) exceed %zu\n", ptr->size, sizeof(buf));
			exit(ERROR);
		}
		if (mib_get_s(ptr->id, buf, ptr->size)) {
			//printf("%s(%d): %s\n",__func__,__LINE__,ptr->name);
			tmp = 0;
			for (i = 0; i<ptr->size; i++)
				tmp |= buf[i];
			if (tmp) {
				if (which)
					*which = ptr;
				return 0;
			}
		}
	}
	return 1;
}

static int wlan_empty_func(int argc, char *argv[])
{
	int verbose = 0, result;
	mib_table_entry_T *ptr;
	if (argc >= 2) {
		if (!strcmp(argv[1], "verbose"))
			verbose = 1;
		else {
			usage();
			return ERROR;
		}
	}

	result = wlan_is_empty(&ptr);
	if (!result && verbose)
		printf("%s not empty\n", ptr->name);
	printf("%d\n", result);
	return 0;
}

static char *trimwhitespace(char *str)
{
  char *end;

  // Trim leading space
  while(isspace((unsigned char)*str)) str++;

  if(*str == 0)  // All spaces?
    return str;

  // Trim trailing space
  end = str + strlen(str) - 1;
  while(end > str && isspace((unsigned char)*end)) end--;

  // Write new null terminator character
  end[1] = '\0';

  return str;
}

// format hhhh,hhhh,... to zero-padded string (ie 000hhhhh000hhhh)
// val is used for output, should be at least 513 (64*8+1) in size.
static int reformat_dpk(char *val) {
	const char *delm = ",";
	char *res, *out, *instr;
	int count = 0, len, ret = 0;
	#define VAL_LEN 8

	instr = strdup(val);

	if (NULL == instr)
		return -1;

	val[0] = '\0';
	out = val;

	res = strtok(instr, delm);
	while (res != NULL) {
		count++;
		len = strlen(res);
		if (len > VAL_LEN) {
			ret = -1;
			goto err1;
		}

		memset(out, 0, VAL_LEN - len);
		out += (VAL_LEN - len);
		memcpy(out, res, len);
		out += len;

		res = strtok(NULL, delm);
	}
	*out = '\0';
	if (count != 64) {
		ret = -1;
		goto err1;
	}

err1:
	free(instr);
	return ret;
}

static int sync_func(int argc, char *argv[])
{
	FILE *fp;
	char buf[1024], *line;
	char key[128], val[1024];
	int c, n_ok = 0, n_fail = 0;

	int force = 0;
	if (argc >= 2) {
		if (!strcmp(argv[1], "force"))
			force = 1;
		else {
			usage();
			return ERROR;
		}
	}

	if (!force && !wlan_is_empty(NULL)) {
		printf("WLAN HS not empty, abort\n");
		return 0;
	}

	fp = fopen(OLD_XML_FILE, "r");
	if (fp != NULL) {
		while (fgets(buf, sizeof(buf)-1, fp)) {
			line = trimwhitespace(buf);
			c = sscanf(line, "<Value Name=\"%127[^\"]\" Value=\"%1023[^\"]\"/>", key, val);
			if (c == 2) {
				if (is_key_item(key)) {
					if (!strncmp("RF_DPK_LUT_", key, 11)) {
						if (reformat_dpk(val)) {
							n_fail++;
							continue;
						}
					}

					c = snprintf(buf, sizeof(buf)-1, MIB " set %s %s", key, val);
					if ((c == sizeof(buf)-1) || system(buf)) {
						n_fail++;
						printf("fail exec: %s\n", buf);
					} else {
						n_ok++;
					}
				}
			}
		}
		fclose(fp);
		snprintf(buf, sizeof(buf)-1, MIB " commit");
		system(buf);
	} else {
		printf("%s not found\n", OLD_XML_FILE);
	}

	printf("%d succeed, %d failed\n", n_ok, n_fail);
	return 0;
}

